<script>
  document.addEventListener('DOMContentLoaded', function () {
    const slides = document.querySelectorAll('.slides li');
    const thumbnails = document.querySelectorAll('.thumbnails a');
    const defaultSlideId = 'slide5'; // Başlangıçta gösterilecek slide'ın ID'si
  
    // Tüm thumbnail bağlantılarına tıklama olayı ekle
    thumbnails.forEach(anchor => {
      anchor.addEventListener('click', function (e) {
        e.preventDefault();
  
        const targetId = this.getAttribute('data-target');
        const targetSlide = document.getElementById(targetId);
  
        if (targetSlide) {
          // Hedef slide'ı aktif yap
          targetSlide.style.display = "block";
          targetSlide.classList.add('active', 'animate');
  
          // Diğer slide'ları pasif yap
          slides.forEach(slide => {
            if (slide !== targetSlide) {
              slide.style.display = "none";
              slide.classList.remove('active', 'animate');
            }
          });
        }
      });
    });
  
    // Sayfa yüklendiğinde varsayılan slide'ı göster
    const defaultSlide = document.getElementById(defaultSlideId);
    if (defaultSlide) {
      defaultSlide.style.display = "block";
      defaultSlide.classList.add('active', 'animate');
      slides.forEach(slide => {
        if (slide !== defaultSlide) {
          slide.style.display = "none";
          slide.classList.remove('active', 'animate');
        }
      });
    }
  
    // Hash değişikliklerini yönet
    window.addEventListener('hashchange', function (e) {
      e.preventDefault();
  
      const targetSlide = document.querySelector(window.location.hash);
      if (targetSlide) {
        slides.forEach(slide => {
          slide.style.display = "none";
          slide.classList.remove('active', 'animate');
        });
  
        targetSlide.style.display = "block";
        targetSlide.classList.add('active', 'animate');
      }
    });
  });
  </script>


